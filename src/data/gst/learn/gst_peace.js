  <p><span class="learn-keyword">Peace Studies</span> situates Nigerian conflicts within a global architecture of international law, regional organizations, and human rights frameworks. Understanding the <span class="learn-keyword">United Nations</span> peace system, international criminal justice, <span class="learn-keyword">ECOWAS</span>, the <span class="learn-keyword">African Union</span>, international humanitarian law, and global human rights frameworks provides the context within which Nigerian peacebuilding operates. Nigeria is simultaneously a recipient of these mechanisms and a major contributor — as one of the world's largest UN peacekeeping contributors and a key ECOWAS actor.</p>
