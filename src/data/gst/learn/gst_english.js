<ul class="learn-list">
  <li><strong>Receiving:</strong> The physical stage where sound waves enter the ear and are transmitted to the brain. This is the hearing stage — purely sensory.</li>
  <li><strong>Attending:</strong> The listener consciously focuses attention on specific sounds or a speaker. We filter out competing stimuli and direct mental energy toward the message.</li>
  <li><strong>Understanding:</strong> The listener assigns meaning to the message, drawing on prior knowledge, context, and language skills. This is where comprehension happens.</li>
  <li><strong>Responding:</strong> The listener provides feedback — nodding, asking questions, taking notes, or giving verbal responses. This confirms that listening has occurred.</li>
  <li><strong>Remembering:</strong> The listener stores the information in memory for future use. Effective note-taking greatly enhances this stage.</li>
</ul>

