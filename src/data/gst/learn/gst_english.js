<p class="learn-p"><span class="learn-keyword">Intensive reading</span> is slow, careful, and detailed reading for thorough understanding. The reader reads every word, analyses sentence structure, looks up unfamiliar vocabulary, and makes notes. This type is used for studying textbooks, analysing literary texts, reading legal documents, or understanding complex academic papers. Quality, not speed, is the goal.</p>

